	HashMap<Integer, Path2D> GetRectList(int year) {
		HashMap<Integer, Path2D> rectList = new HashMap<Integer, Path2D>();
		Iterator<String> iter = SurveyData.iterator();
		while (iter.hasNext()) {
			String data = iter.next();
			if (_GetYearFromSurveyData(data) == year) {
				rectList.put(_GetIDFromSurveyData(data), _GetPathFromSurveyData(data));
			}
		}
		return rectList;
	}

	
	private Path2D.Double _GetPathFromSurveyData(String data) {
		Path2D.Double path = new Path2D.Double();
		
		String[] tokens = data.split(":");
		String[] xy  = null;

		xy = tokens[2].split(",");
		path.moveTo(Double.valueOf(xy[0]), Double.valueOf(xy[1]));
		xy = tokens[3].split(",");
		path.lineTo(Double.valueOf(xy[0]), Double.valueOf(xy[1]));
		xy = tokens[4].split(",");
		path.lineTo(Double.valueOf(xy[0]), Double.valueOf(xy[1]));
		xy = tokens[5].split(",");
		path.lineTo(Double.valueOf(xy[0]), Double.valueOf(xy[1]));
		
		return path;		
	}
	
	private int _GetYearFromSurveyData(String data) {
		String[] tokens = data.split(":");
		return Integer.valueOf(tokens[0]);
	}

	private int _GetIDFromSurveyData(String data) {
		String[] tokens = data.split(":");
		return Integer.valueOf(tokens[1]);
	}
	
	
